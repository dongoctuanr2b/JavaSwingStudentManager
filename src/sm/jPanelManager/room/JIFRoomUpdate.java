    //fill data ra bảng
    private void fillData() {
        List<Room> rooms = roomDAO.getAll();

        DefaultTableModel dtm = new DefaultTableModel();

        dtm.addColumn("ID");
        dtm.addColumn("Room name");

        for (Room room : rooms) {
            Vector<Object> vector = new Vector();
            vector.add(room.getIdRoom());
            vector.add(room.getRoomName());

            dtm.addRow(vector);
        }

        JPRoomManager.jTableRoom.setModel(dtm);
    }

    //validate form update class
    private boolean validateRoomUpdate() {
        boolean flag = true;

        //check className phải là duy nhất ko được trùng
        List<Room> rooms = roomDAO.getAll();

        int index = JPRoomManager.jTableRoom.getSelectedRow();
        TableModel model = JPRoomManager.jTableRoom.getModel();

        if (flag) {
            for (Room room : rooms) {
                String getRowClick = model.getValueAt(index, 1).toString();
                String getJTextRoomN = jTextFieldRoomN.getText();
                if ((getJTextRoomN.equalsIgnoreCase(room.getRoomName()))
                        && (!getRowClick.equalsIgnoreCase(getJTextRoomN))) {
                    JOptionPane.showMessageDialog(rootPane, "Room name " + getJTextRoomN + " already exist!!!", "Warning", JOptionPane.OK_OPTION);

                    flag = false;
                }
            }
        }

        if (flag) {
            for (Room room : rooms) {
                if ((jTextFieldRoomN.getText().equalsIgnoreCase(room.getRoomName()))) {
                    int yes = JOptionPane.showConfirmDialog(rootPane, "Room name " + jTextFieldRoomN.getText() + " already exist. Do you want to change?", "Warning", JOptionPane.YES_NO_OPTION);
                    if (yes == JOptionPane.YES_OPTION) {
                        flag = false;
                    } else {
                        flag = true;
                    }
                }
            }
        }

        //nếu className không bị trùng thì flag vẫn = true rồi check tiếp
        if (flag) {
            if (jTextFieldRoomN.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "One Or More Empty Field!!!", "Warning", JOptionPane.ERROR_MESSAGE);

                return false;
            } else {
                return true;
            }
        } else {
            return false;
        }
    }

    //get idClass từ bảng
    private int idRoom() {
        int index = JPRoomManager.jTableRoom.getSelectedRow();
        TableModel model = JPRoomManager.jTableRoom.getModel();

        int idRoom = Integer.valueOf(model.getValueAt(index, 0).toString());

        return idRoom;
    }

