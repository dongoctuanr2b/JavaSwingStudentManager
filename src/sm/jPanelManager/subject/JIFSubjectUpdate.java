    //fill data ra bảng
    private void fillData() {
        List<Subject> subjects = subjectDAO.getAll();

        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("ID");
        dtm.addColumn("Subject name");
        dtm.addColumn("Fee");

        NumberFormat nf = NumberFormat.getNumberInstance();
        nf.setGroupingUsed(true);

        for (Subject subject : subjects) {
            Vector<Object> vector = new Vector();
            vector.add(subject.getIdSubject());
            vector.add(subject.getSubjectName());
            vector.add(nf.format(subject.getFee()));

            dtm.addRow(vector);
        }

        jTableSubject.setModel(dtm);
    }

    //validate form update course
    private boolean validateSubjectUpdate() {
        boolean flag = true;

        //check course name phải là duy nhất ko được trùng, chuyển flag = false
        List<Subject> subjects = subjectDAO.getAll();

        int index = jTableSubject.getSelectedRow();
        TableModel model = jTableSubject.getModel();

        if (flag) {
            for (Subject subject : subjects) {
                String getRowClick = model.getValueAt(index, 1).toString();
                String getJTextSubjectN = jTextFieldSubjectN.getText();
                if ((getJTextSubjectN.toLowerCase().equals(subject.getSubjectName().toLowerCase()))
                        && (!getRowClick.equalsIgnoreCase(getJTextSubjectN))) {
                    //show dialog, check user click NO thì vẫn lưu course Name cũ
                    JOptionPane.showMessageDialog(rootPane, "Subject name " + getJTextSubjectN + " already exist!!!", "Warning", JOptionPane.OK_OPTION);

                    flag = false;
                }
            }
        }

        if (flag) {
            for (Subject subject : subjects) {
                if (jTextFieldSubjectN.getText().toLowerCase().equals(subject.getSubjectName().toLowerCase())) {
                    //show dialog, check user click NO thì vẫn lưu course Name cũ
                    int yes = JOptionPane.showConfirmDialog(rootPane, "Subject name " + jTextFieldSubjectN.getText() + " already exist!!! Do you want to change?", "Warning", JOptionPane.YES_NO_OPTION);
                    if (yes == JOptionPane.YES_OPTION) {
                        flag = false;
                        break;
                    } else {
                        flag = true;
                        break;
                    }
                }
            }
        }

        //nếu subject name không bị trùng thì flag vẫn = true rồi check tiếp
        if (flag) {
            if (jTextFieldSubjectN.getText().isEmpty() || jTextFieldFee.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "One Or More Empty Field!!!", "Warning", JOptionPane.ERROR_MESSAGE);

                return false;
            } else if (Float.valueOf(jTextFieldFee.getText()) <= 0) {
                JOptionPane.showMessageDialog(rootPane, "You haven't enter fee or your fee incorrectly!!!", "Warning", JOptionPane.ERROR_MESSAGE);

                return false;
            } else {
                return true;
            }
        } else {
            return false;
        }
    }

    //get idSubject từ bảng
    private int idSubject() {
        int index = jTableSubject.getSelectedRow();
        TableModel model = jTableSubject.getModel();

        int idSubject = Integer.valueOf(model.getValueAt(index, 0).toString());

        return idSubject;
    }

