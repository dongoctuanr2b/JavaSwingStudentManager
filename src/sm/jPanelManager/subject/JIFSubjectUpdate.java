    private void jButtonUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUMouseClicked
        // TODO add your handling code here:
        //Check form add student 
        try {
            if (validateSubjectUpdate()) {
                Subject subject = new Subject(
                        idSubject(),
                        jTextFieldSubjectN.getText(),
                        Float.valueOf(jTextFieldFee.getText())
                );

                boolean row = subjectDAO.update(subject);

                if (row) {
                    JOptionPane.showMessageDialog(rootPane, "Subject Data Updated!", "Notification", JOptionPane.INFORMATION_MESSAGE);

                    //khi Update thành công thì show data lên bảng
                    fillData();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error Update Subject!!!", "Warning", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButtonUMouseClicked

