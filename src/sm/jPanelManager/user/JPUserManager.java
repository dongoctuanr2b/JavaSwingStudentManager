    private void jTextFieldSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSearchKeyReleased
        // TODO add your handling code here:
        List<User> users = userDAO.search(jTextFieldSearch.getText());

        DefaultTableModel dtm = new DefaultTableModel();

        dtm.addColumn("idUser");
        dtm.addColumn("Username");
        dtm.addColumn("Password");
        dtm.addColumn("Permission");

        for (User user : users) {
            Vector<Object> vector = new Vector();
            vector.add(user.getIdUser());
            vector.add(user.getUsername());
            vector.add(user.getPassword());
            vector.add(user.getPermissionName());

            dtm.addRow(vector);
        }

        jTableUser.setModel(dtm);

        jTableUser.removeColumn(JPUserManager.jTableUser.getColumnModel().getColumn(2));
        jTableUser.removeColumn(JPUserManager.jTableUser.getColumnModel().getColumn(0));
    }//GEN-LAST:event_jTextFieldSearchKeyReleased

