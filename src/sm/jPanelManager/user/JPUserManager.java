    private void jLabelDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelDeleteMouseClicked
        // TODO add your handling code here:
        try {
            int position = jTableUser.getSelectedRow();
            if (position >= 0) {
                int yes = JOptionPane.showConfirmDialog(this, "Are you sure want to this user delete?", "Warning", JOptionPane.YES_NO_OPTION);
                if (yes == JOptionPane.YES_OPTION) {
                    boolean row = userDAO.delete(idUser());

                    if (row) {
                        JOptionPane.showMessageDialog(this, "User Data Deleted!", "Notification", JOptionPane.INFORMATION_MESSAGE);

                        //khi delete thành công thì show data lên bảng
                        fillData();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error Delete User!!!", "Warning", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jLabelDeleteMouseClicked

