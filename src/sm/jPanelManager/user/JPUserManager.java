    private void jTableUserMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUserMouseReleased
        // TODO add your handling code here:
        List<User> users = userDAO.getAll();

        if (users.size() > 0) {
            int index = jTableUser.getSelectedRow();
            TableModel model = jTableUser.getModel();

            if (jIFUserUpdate != null) {//tránh lỗi NULLpointerEx do chưa khởi tạo IFAddStudentForm mà đã click vào bảng
                JIFUserUpdate.jTextFieldUserN.setText(model.getValueAt(index, 1).toString());
                JIFUserUpdate.jPasswordField.setText(model.getValueAt(index, 2).toString());
                //jcombobox Per name
                for (int i = 0; i < JIFUserUpdate.jComboBoxPer.getItemCount(); i++) {
                    if (JIFUserUpdate.jComboBoxPer.getItemAt(i).toString().
                            equalsIgnoreCase(model.getValueAt(index, 3).toString())) {
                        JIFUserUpdate.jComboBoxPer.setSelectedIndex(i);
                    }
                }
            }
        }
    }//GEN-LAST:event_jTableUserMouseReleased

    private void jTableUserKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableUserKeyReleased
        // TODO add your handling code here:
        List<User> users = userDAO.getAll();

        //get key up and key down để fill dữ liệu ra form add
        if ((evt.getKeyCode() == KeyEvent.VK_UP) || (evt.getKeyCode() == KeyEvent.VK_DOWN)) {
            if (users.size() > 0) {
                int index = jTableUser.getSelectedRow();
                TableModel model = jTableUser.getModel();

                if (jIFUserUpdate != null) {//tránh lỗi NULLpointerEx do chưa khởi tạo IFAddStudentForm mà đã click vào bảng
                    JIFUserUpdate.jTextFieldUserN.setText(model.getValueAt(index, 1).toString());
                    JIFUserUpdate.jPasswordField.setText(model.getValueAt(index, 2).toString());
                    //jcombobox Per name
                    for (int i = 0; i < JIFUserUpdate.jComboBoxPer.getItemCount(); i++) {
                        if (JIFUserUpdate.jComboBoxPer.getItemAt(i).toString().
                                equalsIgnoreCase(model.getValueAt(index, 3).toString())) {
                            JIFUserUpdate.jComboBoxPer.setSelectedIndex(i);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_jTableUserKeyReleased

