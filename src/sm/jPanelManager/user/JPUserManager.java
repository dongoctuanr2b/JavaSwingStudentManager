    //fill data ra bảng
    private void fillData() {

        List<User> users = userDAO.getAll();

        DefaultTableModel dtm = new DefaultTableModel();

        dtm.addColumn("idUser");
        dtm.addColumn("Username");
        dtm.addColumn("Password");
        dtm.addColumn("Permission");

        for (User user : users) {
            Vector<Object> vector = new Vector();
            vector.add(user.getIdUser());
            vector.add(user.getUsername());
            vector.add(user.getPassword());
            vector.add(user.getPermissionName());

            dtm.addRow(vector);
        }

        jTableUser.setModel(dtm);

        jTableUser.removeColumn(JPUserManager.jTableUser.getColumnModel().getColumn(2));
        jTableUser.removeColumn(JPUserManager.jTableUser.getColumnModel().getColumn(0));
    }

    private void setData() {
        List<User> users = userDAO.getAll();

        if (users.size() > 0) {
            int index = jTableUser.getSelectedRow();
            TableModel model = jTableUser.getModel();

            if (jIFUserUpdate != null) {//tránh lỗi NULLpointerEx do chưa khởi tạo IFAddStudentForm mà đã click vào bảng
                JIFUserUpdate.jTextFieldUserN.setText(model.getValueAt(index, 1).toString());
                JIFUserUpdate.jPasswordField.setText(model.getValueAt(index, 2).toString());
                //jcombobox Per name
                for (int i = 0; i < JIFUserUpdate.jComboBoxPer.getItemCount(); i++) {
                    if (JIFUserUpdate.jComboBoxPer.getItemAt(i).toString().
                            equalsIgnoreCase(model.getValueAt(index, 3).toString())) {
                        JIFUserUpdate.jComboBoxPer.setSelectedIndex(i);
                    }
                }
            }
        }
    }

    //get idScore từ bảng
    private String idUser() {
        int index = jTableUser.getSelectedRow();
        TableModel model = jTableUser.getModel();

        String idUser = model.getValueAt(index, 0).toString();

        return idUser;
    }

