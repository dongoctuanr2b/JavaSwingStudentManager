    private void jButtonUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUMouseClicked
        // TODO add your handling code here:
        //Check form update classes
        try {
            if (validateClassUpdate()) {
                Classes classes = new Classes(
                        idClass(),
                        jTextFieldClassN.getText(),
                        Integer.valueOf(jTextFieldNOS.getText())
                );

                boolean row = classesDAO.update(classes);

                if (row) {
                    JOptionPane.showMessageDialog(rootPane, "Class Data Updated!", "Notification", JOptionPane.INFORMATION_MESSAGE);

                    fillData();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error Update Class!!!", "Warning", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButtonUMouseClicked

