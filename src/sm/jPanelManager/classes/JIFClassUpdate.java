    //fill data ra bảng
    private void fillData() {
        List<Classes> listClasses = classesDAO.getAll();

        DefaultTableModel dtm = new DefaultTableModel();

        dtm.addColumn("ID");
        dtm.addColumn("Class Name");
        dtm.addColumn("Number of students");

        for (Classes classes : listClasses) {
            Vector<Object> vector = new Vector();
            vector.add(classes.getIdClass());
            vector.add(classes.getClassName());
            vector.add(classes.getNumberOfStudents());

            dtm.addRow(vector);
        }

        JPClassManager.jTableClass.setModel(dtm);
    }

    //validate form update class
    private boolean validateClassUpdate() {
        boolean flag = true;

        //check className phải là duy nhất ko được trùng
        List<Classes> listClass = classesDAO.getAll();

        int index = JPClassManager.jTableClass.getSelectedRow();
        TableModel model = JPClassManager.jTableClass.getModel();

        if (flag) {
            for (Classes classes : listClass) {
                String getRowClick = model.getValueAt(index, 1).toString();
                String getJTextClassN = jTextFieldClassN.getText();

                if ((getJTextClassN.equalsIgnoreCase(classes.getClassName()))
                        && (!getRowClick.equalsIgnoreCase(getJTextClassN))) {
                    JOptionPane.showMessageDialog(rootPane, "Class name " + getJTextClassN + " already exist!!!", "Warning", JOptionPane.OK_OPTION);

                    flag = false;
                }
            }
        }

        if (flag) {
            for (Classes classes : listClass) {
                if ((jTextFieldClassN.getText().equalsIgnoreCase(classes.getClassName()))) {
                    int yes = JOptionPane.showConfirmDialog(rootPane, "Class name " + classes.getClassName() + " already exist!!! Do you want to change?", "Warning", JOptionPane.YES_NO_OPTION);
                    if (yes == JOptionPane.YES_OPTION) {
                        flag = false;
                        break;
                    } else {
                        flag = true;
                        break;
                    }
                }
            }
        }

        //nếu className không bị trùng thì flag vẫn = true rồi check tiếp
        if (flag) {
            if (jTextFieldClassN.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "One Or More Empty Field!!!", "Warning", JOptionPane.ERROR_MESSAGE);

                return false;
            } else {
                return true;
            }
        } else {
            return false;
        }
    }

    //get idClass từ bảng
    private int idClass() {
        int index = JPClassManager.jTableClass.getSelectedRow();
        TableModel model = JPClassManager.jTableClass.getModel();

        int idClass = Integer.valueOf(model.getValueAt(index, 0).toString());

        return idClass;
    }

